class Product
{
    constructor()
    {
        id: 0;
        name: "";
        description: "";
        price: 0;
        category: "";
    }

    constructor(name, description, price, category)
    {
        this();
        this.name = name;
        this.description = this.description;
        this.price = price;
        this.category = category;
    }

}